class Syain{

    constructor(syaban,sei,mei,tel,address){
        this._syaban = syaban;
        this._sei = sei;
        this._mei = mei;
        this._tel = tel;
        this._address = address;
    }

    get syaban(){
        return this._syaban
    }

    get sei(){
        return this._sei
    }

    get mei(){
        return this._mei
    }

    get tel(){
        return this._tel
    }

    get address(){
        return this._address
    }
    
}